package com.atguigu.crud.bean;

import javax.validation.constraints.Pattern;


public class Employee {
    private Integer empId;
    
    //自定义校验
    @Pattern(regexp="^[a-z0-9_-]{3,16}$",message="用户名必须是由3-16的数字和字母组成-后端jsr303")
    private String empName;

    private String gender;
    
//    @Email //也可以自己定义
    @Pattern(regexp="^([a-z0-9_\\.-]+)@([\\da-z\\.-]+)\\.([a-z\\.]{2,6})$",message="邮箱格式不正确-后端:jsr303")
    private String email;

    private Integer deptId;
    
    //员工和部门是一对一
    private Department department;
    
    
    
    public Employee() {
		super();
	}
    //不带部门的
	public Employee(Integer empId, String empName, String gender, String email, Integer deptId) {
		super();
		this.empId = empId;
		this.empName = empName;
		this.gender = gender;
		this.email = email;
		this.deptId = deptId;
	}

	public Department getDepartment() {
		return department;
	}

	public void setDepartment(Department department) {
		this.department = department;
	}

	public Integer getEmpId() {
        return empId;
    }

    public void setEmpId(Integer empId) {
        this.empId = empId;
    }

    public String getEmpName() {
        return empName;
    }

    public void setEmpName(String empName) {
        this.empName = empName == null ? null : empName.trim();
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender == null ? null : gender.trim();
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email == null ? null : email.trim();
    }

    public Integer getDeptId() {
        return deptId;
    }

    public void setDeptId(Integer deptId) {
        this.deptId = deptId;
    }
	@Override
	public String toString() {
		return "Employee [empId=" + empId + ", empName=" + empName + ", gender=" + gender + ", email=" + email
				+ ", deptId=" + deptId + ", department=" + department + "]";
	}
}